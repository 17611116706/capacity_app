package com.example.schoolapp.mvp.model.minebean;

public class LogoutEntity {


    /**
     * code : 200
     * token :
     * msg : null
     * data : 退出成功
     */

    private int code;
    private String token;
    private String msg;
    private String data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
